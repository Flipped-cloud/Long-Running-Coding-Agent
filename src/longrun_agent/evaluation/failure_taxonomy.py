def termination_reason_from_status(status: str) -> TerminationReason:
    mapping = {
        RunStatus.COMPLETED.value: TerminationReason.COMPLETED,
        "candidate_complete": TerminationReason.COMPLETED,
        "verified": TerminationReason.COMPLETED,
        RunStatus.CONTEXT_BUDGET_EXHAUSTED.value: TerminationReason.CONTEXT_LIMIT,
        RunStatus.PROTOCOL_ERROR.value: TerminationReason.INVALID_FORMAT,
        RunStatus.PROVIDER_ERROR.value: TerminationReason.PROVIDER_ERROR,
        RunStatus.TIME_LIMIT_REACHED.value: TerminationReason.TIME_LIMIT,
        "session_limit_reached": TerminationReason.SESSION_LIMIT,
        "time_limit_reached": TerminationReason.TIME_LIMIT,
        "verification_inconclusive": TerminationReason.VERIFICATION_INCONCLUSIVE,
    }
    return mapping.get(status, TerminationReason.UNKNOWN)
